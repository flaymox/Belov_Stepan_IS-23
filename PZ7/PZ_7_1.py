# Дан символ C. Программа выводит два символа, первый из которых предшествует символу C в
# кодовой таблице, а второй следует за символом C.
# Ввод символа с клавиатуры
input_char = input("Введите символ: ")

# Проверка, что введен ровно один символ
if len(input_char) != 1:
    print("Введите ровно один символ")
else:
    # Получение кода символа
    char_code = ord(input_char)

    # Получение предыдущего и следующего символа в кодовой таблице
    prev_char = chr(char_code - 1)
    next_char = chr(char_code + 1)

    # Вывод результата
    print(f"Предшествующий символ: {prev_char}\nСледующий символ: {next_char}")
